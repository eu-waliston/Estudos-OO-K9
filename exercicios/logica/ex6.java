package exercicios.logica;

public class ex6 {
    public static void main(String[] args) {
        String linha = "*";
        for(int contador = 1; contador <= 10; contador++) {
            System.out.println(linha);
            int resto = contador % 4;
            if(resto == 0) {
                linha = "*";
            } else {
                linha += "*";
            }
        }
    }
}
